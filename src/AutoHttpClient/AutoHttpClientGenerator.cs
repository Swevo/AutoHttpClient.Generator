    {
        Get,
        Post,
        Put,
        Delete,
        Patch,
    }

    private enum ParameterKind
    {
        Route,
        Query,
        Header,
        Body,
        CancellationToken,
    }

    private enum ReturnHandlingKind
    {
        None,
        Json,
        Response,
    }
