    private static readonly DiagnosticDescriptor AH001 = new(
        id: "AH001",
        title: "HTTP method attribute missing",
        messageFormat: "Method '{0}' on [HttpClient] interface '{1}' has no HTTP method attribute ([Get], [Post], etc.) — it will not be generated",
        category: "AutoHttpClient",
        defaultSeverity: DiagnosticSeverity.Warning,
        isEnabledByDefault: true);

    private static readonly DiagnosticDescriptor AH002 = new(
        id: "AH002",
        title: "Route template parameter missing method parameter",
        messageFormat: "Route template parameter '{0}' in '{1}' on '{2}' has no matching method parameter",
        category: "AutoHttpClient",
        defaultSeverity: DiagnosticSeverity.Warning,
        isEnabledByDefault: true);

    private static readonly DiagnosticDescriptor AH003 = new(
        id: "AH003",
        title: "Multiple [Body] parameters",
